;---------------------------------------------------------------------------------------------------
; Вторичная таблица прерываний - перенаправление int 6Dh в int 0Dh
; 
; Назначение: Этот обработчик перенаправляет вызов прерывания 6Dh
; на стандартное прерывание 0Dh (IRQ5).
;
; Архитектура прерываний МС1502:
;   - ПЕРВИЧНАЯ таблица (08h-0Fh): Стандартные IRQ0-IRQ7 IBM PC
;   - ВТОРИЧНАЯ таблица (68h-6Fh): Аппаратные прерывания МС1502
;
; Прерывание 0Dh (IRQ5) в стандартном PC/XT:
;  - В оригинальном IBM PC XT используется для контроллера жесткого диска
;  - В системах без жесткого диска может использоваться для LPT2 (второй принтерный порт)
;  - Часто зарезервировано для пользовательских устройств
;  - В МС1502 может использоваться для аналогичного оборудования или специфичных устройств
;
; Особенность МС1502: Аппаратное прерывание поступает в вторичную таблицу (6Dh),
; которая затем вызывает обработчик из первичной таблицы (0Dh).
; Это обеспечивает совместимость со стандартным ПО, ожидающим обработку IRQ5.
;---------------------------------------------------------------------------------------------------
proc		int_6Dh	near
                int	0Dh		; Вызываем обработчик IRQ5 из ПЕРВИЧНОЙ таблицы
                iret			; Возврат из прерывания ВТОРИЧНОЙ таблицы
endp		int_6Dh
;---------------------------------------------------------------------------------------------------
