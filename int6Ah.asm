;------------------------------------------------------------------------
; Вторичная таблица прерываний - перенаправление int 6Ah в int 0Ah
; 
; Назначение: Этот обработчик перенаправляет вызов прерывания 6Ah
; на стандартное прерывание 0Ah (IRQ2).
;
; Архитектура прерываний МС1502:
;   - ПЕРВИЧНАЯ таблица (08h-0Fh): Стандартные IRQ0-IRQ7 IBM PC
;   - ВТОРИЧНАЯ таблица (68h-6Fh): Аппаратные прерывания МС1502
;
; Особенность МС1502: Вторичная таблица используется для аппаратных
; прерываний, которые затем вызывают первичную таблицу.
; Это сделано для совместимости: если ПО (например, Windows 3.0) подменит
; обработчики в первичной таблице, аппаратные прерывания все равно будут
; работать через вторичную таблицу.
;
; Прерывание 0Ah (IRQ2) в стандартном PC/XT:
;  - Зарезервировано для пользователя (иногда используется для каскадирования
;    контроллеров прерываний в AT)
;  - В МС1502 может использоваться для специфичного оборудования
;------------------------------------------------------------------------
proc		int_6Ah near
                int	0Ah		; Вызываем обработчик из ПЕРВИЧНОЙ таблицы
                iret			; Возврат из прерывания ВТОРИЧНОЙ таблицы
endp		int_6Ah
;---------------------------------------------------------------------------------------------------