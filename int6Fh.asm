;---------------------------------------------------------------------------------------------------
; Вторичная таблица прерываний - перенаправление int 6Fh в int 0Fh
; 
; Назначение: Этот обработчик перенаправляет вызов прерывания 6Fh
; на стандартное прерывание 0Fh (IRQ7).
;
; Архитектура прерываний МС1502:
;   - ПЕРВИЧНАЯ таблица (08h-0Fh): Стандартные IRQ0-IRQ7 IBM PC
;   - ВТОРИЧНАЯ таблица (68h-6Fh): Аппаратные прерывания МС1502
;
; Прерывание 0Fh (IRQ7) в стандартном PC/XT:
;  - В стандартном PC используется для параллельного порта LPT1 (принтер)
;  - Генерируется принтером при готовности или возникновении ошибки
;
; Особенность МС1502: Как и в случае с дисководом, контроллер принтера
; в МС1502 НЕ использует прерывания для своей работы. Работа с принтером
; осуществляется в режиме опроса состояния (polling mode).
;
; Прерывание IRQ7 (вектор 6Fh в МС1502) свободно и может использоваться:
;  1. Для совместимости с программным обеспечением, устанавливающим обработчики на IRQ7
;  2. Для пользовательских устройств или плат расширения
;  3. Как резервный вектор прерывания
;
; Режим опроса (polling) принтера в МС1502:
;  - Программа периодически читает регистр статуса принтера (порт 6Ah)
;  - Проверяет готовность принтера (бит BUSY)
;  - Отправляет данные, когда принтер готов
;  - Этот подход используется в обработчике int 17h
;
; Данный обработчик оставлен для обеспечения полной совместимости
; со стандартной архитектурой прерываний IBM PC.
;---------------------------------------------------------------------------------------------------
proc		int_6Fh near
                int	0Fh		; Вызываем обработчик IRQ7 из ПЕРВИЧНОЙ таблицы
                iret			; Возврат из прерывания ВТОРИЧНОЙ таблицы
endp		int_6Fh
; ---------------------------------------------------------------------------