; ---------------------------------------------------------------------------
; Заглушка для неиспользуемых или нереализованных прерываний
;
; Назначение: Эта процедура используется для обработки прерываний, которые:
;   1. Не реализованы в данной версии BIOS
;   2. Зарезервированы для будущего использования
;   3. Не должны выполнять никаких действий
;
; При вызове любого прерывания, вектор которого указывает на эту процедуру,
; процессор просто вернется к выполнению прерванной программы без каких-либо действий.
;
; Важно: Такие "пустые" обработчики необходимы для предотвращения сбоев системы,
; если программа случайно вызовет нереализованное прерывание.
; ---------------------------------------------------------------------------
proc		dummy_int near
                iret		; Просто возвращаем управление из прерывания
				; IRET восстанавливает флаги и адрес возврата из стека
endp		dummy_int
