;---------------------------------------------------------------------------------------------------
; Вторичная таблица прерываний - перенаправление int 6Bh в int 0Bh
; 
; Назначение: Этот обработчик перенаправляет вызов прерывания 6Bh
; на стандартное прерывание 0Bh (IRQ3).
;
; Архитектура прерываний МС1502:
;   - ПЕРВИЧНАЯ таблица (08h-0Fh): Стандартные IRQ0-IRQ7 IBM PC
;   - ВТОРИЧНАЯ таблица (68h-6Fh): Аппаратные прерывания МС1502
;
; Прерывание 0Bh (IRQ3) в стандартном PC:
;  - Используется для последовательного порта COM2 (или COM4)
;  - В МС1502 может использоваться для аналогичного оборудования
;
; Особенность МС1502: Аппаратное прерывание поступает в вторичную таблицу (6Bh),
; которая затем вызывает обработчик из первичной таблицы (0Bh).
; Это обеспечивает совместимость: если ПО подменит обработчик в первичной таблице,
; аппаратные прерывания все равно будут корректно обрабатываться.
;---------------------------------------------------------------------------------------------------
proc		int_6Bh	near
                int	0Bh		; Вызываем обработчик COM2 из ПЕРВИЧНОЙ таблицы
                iret			; Возврат из прерывания ВТОРИЧНОЙ таблицы
endp		int_6Bh
;---------------------------------------------------------------------------------------------------
