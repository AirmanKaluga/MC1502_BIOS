;---------------------------------------------------------------------------------------------------
; Вторичная таблица прерываний - перенаправление int 6Eh в int 0Eh
; 
; Назначение: Этот обработчик перенаправляет вызов прерывания 6Eh
; на стандартное прерывание 0Eh (IRQ6).
;
; Важное замечание: В МС1502 контроллер дисковода построен на микросхеме КР1818ВГ93
; (советский аналог WD1793) и НЕ использует прерывания для своей работы.
; Вместо этого используется режим опроса состояния (polling mode).
;
; Архитектура прерываний МС1502:
;   - ПЕРВИЧНАЯ таблица (08h-0Fh): Стандартные IRQ0-IRQ7 IBM PC
;   - ВТОРИЧНАЯ таблица (68h-6Fh): Аппаратные прерывания МС1502
;
; Прерывание 0Eh (IRQ6) в стандартном PC/XT:
;  - Используется для контроллера дисковода гибких дисков (FDC)
;  - Генерируется контроллером NEC µPD765 (или совместимым) при завершении операции
;
; Особенность МС1502: Хотя контроллер дисковода КР1818ВГ93 не генерирует прерываний,
; данный обработчик оставлен для:
;  1. Совместимости с программным обеспечением, ожидающим прерывание 0Eh
;  2. Обработки возможных аппаратных прерываний от других устройств, 
;     использующих вектор 6Eh в МС1502
;  3. Резерва на случай использования других контроллеров дисковода
;
; Режим опроса (polling) контроллера КР1818ВГ93:
;  - Программа периодически читает регистр статуса контроллера
;  - При готовности данных/завершении операции программа обрабатывает результат
;  - Этот подход проще, но менее эффективен, чем использование прерываний
;---------------------------------------------------------------------------------------------------
proc		int_6Eh  near
                int	0Eh		; Вызываем обработчик IRQ6 из ПЕРВИЧНОЙ таблицы
                iret			; Возврат из прерывания ВТОРИЧНОЙ таблицы
endp		int_6Eh
; ---------------------------------------------------------------------------
