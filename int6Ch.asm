;---------------------------------------------------------------------------------------------------
; Вторичная таблица прерываний - перенаправление int 6Ch в int 0Ch
; 
; Назначение: Этот обработчик перенаправляет вызов прерывания 6Ch
; на стандартное прерывание 0Ch (IRQ4).
;
; Архитектура прерываний МС1502:
;   - ПЕРВИЧНАЯ таблица (08h-0Fh): Стандартные IRQ0-IRQ7 IBM PC
;   - ВТОРИЧНАЯ таблица (68h-6Fh): Аппаратные прерывания МС1502
;
; Прерывание 0Ch (IRQ4) в стандартном PC:
;  - Используется для последовательного порта COM1 (или COM3)
;  - Часто используется для мыши, модема или других последовательных устройств
;  - В МС1502 может использоваться для аналогичного оборудования
;
; Особенность МС1502: Аппаратное прерывание от контроллера COM1 поступает в
; вторичную таблицу (6Ch), которая затем вызывает обработчик из первичной
; таблицы (0Ch). Это обеспечивает совместимость со стандартным ПО.
;---------------------------------------------------------------------------------------------------
proc		int_6Ch  near
                int	0Ch		; Вызываем обработчик COM1 из ПЕРВИЧНОЙ таблицы
                iret			; Возврат из прерывания ВТОРИЧНОЙ таблицы
endp		int_6Ch
;---------------------------------------------------------------------------------------------------
